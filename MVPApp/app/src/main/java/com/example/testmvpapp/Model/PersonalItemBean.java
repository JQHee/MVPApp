package com.example.testmvpapp.Model;

public class PersonalItemBean  {

    private String title;
    private int id;
    private int icon;
    private int right_icon;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public int getRight_icon() {
        return right_icon;
    }

    public void setRight_icon(int right_icon) {
        this.right_icon = right_icon;
    }
}
